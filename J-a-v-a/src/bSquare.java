public class bSquare {
    public static void main(String[] args) {
        int n = 0;
        System.out.println("ББББББББББББББББББББ");
        while (n < 8) {
            System.out.print("Б");
            int m = 0;
            while (m < 18) {
                System.out.print(" ");
                m++;
            }
            System.out.print("Б");
            System.out.println();
            n++;
        }
        System.out.println("ББББББББББББББББББББ");
    }
}
